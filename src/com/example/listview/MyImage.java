package com.example.listview;

public class MyImage{
	private int image;
	private String label;
	public MyImage(int image, String label) {
		super();
		this.image = image;
		this.label = label;
	}
	public int getImage() {
		return image;
	}
	public void setImage(int image) {
		this.image = image;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	
}
